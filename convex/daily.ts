/**
 * Get combined pending tasks for a project family (parent + subprojects)
 */
export const getProjectFamilyPendingTasks = query({
  args: { projectId: v.id("projects") },
  handler: async (ctx, args) => {
    const parent = await ctx.db.get(args.projectId);
    if (!parent || parent.isSubproject) return null;

    // Get parent's own tasks
    const parentTasks = await ctx.db
      .query("tasks")
      .withIndex("by_project_completion", (q) =>
        q.eq("projectId", args.projectId).eq("completed", false)
      )
      .order("desc")
      .collect();

    // Get subprojects
    const subprojects = await ctx.db
      .query("projects")
      .withIndex("by_parent", (q) => q.eq("parentProjectId", args.projectId))
      .filter((q) => q.eq(q.field("status"), "active"))
      .collect();

    // Get tasks for each subproject
    const subprojectTasks = await Promise.all(
      subprojects.map(async (sub) => {
        const tasks = await ctx.db
          .query("tasks")
          .withIndex("by_project_completion", (q) =>
            q.eq("projectId", sub._id).eq("completed", false)
          )
          .order("desc")
          .collect();
        return {
          subprojectId: sub._id,
          subprojectName: sub.name.replace(`${parent.name} / `, ""), // Clean name
          tasks: tasks.map((t) => ({
            id: t._id,
            text: t.text,
            autoGenerated: t.autoGenerated,
          })),
          count: tasks.length,
        };
      })
    );

    // Filter out empty subprojects
    const nonEmptySubprojects = subprojectTasks.filter((s) => s.count > 0);

    const totalPending =
      parentTasks.length + nonEmptySubprojects.reduce((sum, s) => sum + s.count, 0);

    return {
      project: {
        id: parent._id,
        name: parent.name,
        group: parent.group,
      },
      parentTasks: parentTasks.map((t) => ({
        id: t._id,
        text: t.text,
        autoGenerated: t.autoGenerated,
      })),
      subprojects: nonEmptySubprojects,
      totalPending,
      hasSubprojects: subprojects.length > 0,
    };
  },
});

/**
 * Get all active parent projects (excluding subprojects)
 */
export const getParentProjects = query({
  args: {
    group: v.optional(v.union(v.literal("important"), v.literal("hobbies"))),
  },
  handler: async (ctx, args) => {
    let query = ctx.db
      .query("projects")
      .withIndex("by_status", (q) => q.eq("status", "active"));

    if (args.group) {
      query = query.filter((q) => q.eq(q.field("group"), args.group));
    }

    const projects = await query.order("desc").collect();

    // Only return parent projects (not subprojects)
    return projects
      .filter((p) => !p.isSubproject)
      .map((p) => ({
        id: p._id,
        name: p.name,
        group: p.group,
      }));
  },
});

/**
 * Get summary: ALL projects or filtered by group with their pending tasks
 */
export const getEveningSummary = query({
  args: {
    group: v.optional(v.union(v.literal("important"), v.literal("hobbies"))),
  },
  handler: async (ctx, args) => {
    // Get parent projects (filtered by group if specified)
    let allParents = await ctx.db
      .query("projects")
      .withIndex("by_status", (q) => q.eq("status", "active"))
      .collect();

    if (args.group) {
      allParents = allParents.filter((p) => p.group === args.group);
    }

    const parentProjects = allParents.filter((p) => !p.isSubproject);

    const projectSummaries = [];
    let grandTotalPending = 0;

    for (const parent of parentProjects) {
      // Get parent's tasks
      const parentTasks = await ctx.db
        .query("tasks")
        .withIndex("by_project_completion", (q) =>
          q.eq("projectId", parent._id).eq("completed", false)
        )
        .order("desc")
        .collect();

      // Get subprojects and their tasks
      const subprojects = await ctx.db
        .query("projects")
        .withIndex("by_parent", (q) => q.eq("parentProjectId", parent._id))
        .filter((q) => q.eq(q.field("status"), "active"))
        .collect();

      const subprojectData = [];
      for (const sub of subprojects) {
        const tasks = await ctx.db
          .query("tasks")
          .withIndex("by_project_completion", (q) =>
            q.eq("projectId", sub._id).eq("completed", false)
          )
          .order("desc")
          .collect();

        if (tasks.length > 0) {
          subprojectData.push({
            name: sub.name.replace(`${parent.name} / `, ""),
            tasks: tasks.map((t) => t.text),
            count: tasks.length,
          });
        }
      }

      const totalPending = parentTasks.length + subprojectData.reduce((sum, s) => sum + s.count, 0);

      if (totalPending > 0) {
        projectSummaries.push({
          projectName: parent.name,
          group: parent.group,
          parentTasks: parentTasks.map((t) => t.text),
          subprojects: subprojectData,
          totalPending,
          hasSubprojects: subprojects.length > 0,
        });
        grandTotalPending += totalPending;
      }
    }

    // Sort: Important first, then by pending count
    projectSummaries.sort((a, b) => {
      if (a.group === "important" && b.group !== "important") return -1;
      if (a.group !== "important" && b.group === "important") return 1;
      return b.totalPending - a.totalPending;
    });

    return {
      projects: projectSummaries,
      totalProjects: projectSummaries.length,
      grandTotalPending,
    };
  },
});

