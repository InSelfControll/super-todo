export const morningSync = internalAction({
  args: {
    projectId: v.optional(v.id("projects")),
  },
  handler: async (ctx, args) => {
    if (!args.projectId) {
      return await ctx.runAction("daily:morningSyncAllImportant", {});
    }

    const familyData = await ctx.runQuery(
      // @ts-ignore - internal query
      "daily:getProjectFamilyPendingTasks",
      { projectId: args.projectId }
    );

    if (!familyData) {
      return { notified: false, reason: "project_not_found" };
    }

    const allTasks = buildCombinedTaskList(familyData);

    await ctx.runAction("notifications:notifyProjectFamily", {
      projectName: familyData.project.name,
      group: familyData.project.group,
      totalPending: familyData.totalPending,
      tasks: allTasks.slice(0, 20),
      hasSubprojects: familyData.hasSubprojects,
      timeOfDay: "morning",
      isUrgent: true,
    });

    return {
      notified: true,
      project: familyData.project,
      totalPending: familyData.totalPending,
    };
  },
});

