export const getEveningReport = query({
  args: {},
  handler: async (ctx) => {
    const startOfDay = new Date();
    startOfDay.setHours(0, 0, 0, 0);
    const startOfDayTimestamp = startOfDay.getTime();

    const projects = await ctx.db
      .query("projects")
      .withIndex("by_status", (q) => q.eq("status", "active"))
      .collect();

    const completedTasks = await ctx.db
      .query("tasks")
      .withIndex("by_completion", (q) => q.eq("completed", true))
      .collect();

    const todayCompletions = completedTasks.filter(
      (t) => t.completedAt && t.completedAt >= startOfDayTimestamp
    );

    const byProject = new Map();
    for (const task of todayCompletions) {
      const project = projects.find((p) => p._id === task.projectId);
      if (!project) continue;

      // Use parent name if subproject
      const displayName = project.isSubproject && project.parentProjectId
        ? (await ctx.db.get(project.parentProjectId))?.name || project.name
        : project.name;

      if (!byProject.has(displayName)) {
        byProject.set(displayName, []);
      }
      byProject.get(displayName).push(task.text);
    }

    return {
      date: startOfDay.toISOString().split("T")[0],
      totalCompleted: todayCompletions.length,
      byProject: Array.from(byProject.entries()).map(([name, tasks]) => ({
        projectName: name,
        tasks,
        count: tasks.length,
      })),
    };
  },
});

export const eveningReport = internalAction({
  args: {},
  handler: async (ctx) => {
    const report = await ctx.runQuery(
      // @ts-ignore - internal query
      "daily:getEveningReport",
      {}
    );

    const allTasks = report.byProject.flatMap((p: { tasks: string[] }) => p.tasks);

    await ctx.runAction("notifications:notifyEveningReport", {
      completedCount: report.totalCompleted,
      completedTasks: allTasks.slice(0, 10),
    });

    return {
      notified: true,
      report,
    };
  },
});

