/**
 * Build a flat task list from project family data
 * Format: [Parent Task] or [Subproject / Task]
 */
function buildCombinedTaskList(familyData: any): string[] {
  const tasks: string[] = [];

  // Parent tasks (no prefix)
  for (const task of familyData.parentTasks) {
    tasks.push(task.text);
  }

  // Subproject tasks (with prefix)
  for (const sub of familyData.subprojects) {
    for (const task of sub.tasks) {
      tasks.push(`[${sub.subprojectName}] ${task.text}`);
    }
  }

  return tasks;
}

function sleep(ms: number): Promise<void> {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

